# 畫出下列三種排列的星星圖形。
#
#  (1)	*         (2)  * * * * *    (3)    *
#       * *              * * * *          *  *
#       * * *              * * *         *  *  *
#       * * * *              * *        *  *  *  *
#       * * * * *              *       *  *  *  *  *


for j in range(1, 6):
    print('*' * j)

print("============================================")

for i in range(5):
    print(" " * (i), end='')
    print("*" * (5 - i))

print("============================================")

for i in range(5):
    print(" " * (4-i), end='')
    print("* " * (i+1))